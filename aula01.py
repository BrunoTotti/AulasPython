#para saídas de dados, usamos o comando print()
#sempre que quiser escrever um texto, devemos utilizar as aspas (duplas ou simples) ""/''
print('Olá mundo!')
print("Meu nome é Bruno")

#operações matemáticas
print("2 + 3") #numeros quandlo utilizados co maspas, os viram textos
print(2 + 3) #aqui fazemos um calculo de soma entre 2 e 3

print('3 * 2') #exibimos o texto 3 * 2
print(3 * 2) #exibimos o RESULTADO entre 3 * 2, que seria 6

print('10 - 2') #exibimos o texto 10 - 2
print(10 - 2) #exibimos o RESULTADO entre 10 - 2, que seria 8

print('14 / 2') #exibimos o texto 14 / 2
print(14 / 2) #exibimos o RESULTADO entre 14 / 2, que seria 7

#as divisoes no python sempre vão ter um número real

#o operador é um módulo que só retorna o resto da divisão
print('10 % 2') #exibe o texto 10 % 2
print(10 % 2) #exibe o RESULTADO de 10 % 2, qu seria 0

print('10 % 4') #exibe o texto 10 % 4 
print(10 % 4) #exibe o RESULTADO de 10% 4, que seria 2

#para sabermos a arte inteira da divisão usamos o //
print("6 // 4") #exibe o texto 6 // 4
print(6 // 4) #exibe o resultado de 6 // 4, que seria 1

